#######  string in python  #########
z = ' nguyen van khoa '
print(z[1]) # print charater at position 1
print(z[3:5]) #print charater from at position 3 to position 5
print(z.strip()) #removes  any whitespace from the beginning or the end
print(len(z))  #return length of a string
print(z.lower()) # return the string in lower case
print(z.upper()) # return the string in upper case
print(z.replace('h','t')) # replace  a string with another string
print(z.split('v')) # string in to substring  if it finds  instance  of  the separator